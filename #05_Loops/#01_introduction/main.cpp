#include <bits/stdc++.h>
using namespace std;

int main() {
  // Print 1 to 10
  cout << "1" << endl;
  cout << "2" << endl;
  cout << "3" << endl;
  cout << "4" << endl;
  cout << "5" << endl;
  cout << "6" << endl;
  cout << "7" << endl;
  cout << "8" << endl;
  cout << "9" << endl;
  cout << "10" << endl << endl;

  // Print 1 to 10 using for loop
  for (int i = 1; i <= 10; i++) {
    cout << i << endl;
  }

  return 0;
}
